package day04;

public class Test33 {

	public static void main(String[] args) {
		
		//while
		byte a = 0; 	// 초기식
		while (a<5) {	// 조건식 : 반복할때마다 먼저 검사하고 TRUE면 실행
			System.out.println("안녕하세요");	// 반복시킬 명령
			a++;	// 증감식
		}
		
		// 0~ 5 출력
		int n = 0;
		while (n <= 5) {
			System.out.println(n);
			n++;
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
